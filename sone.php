       <div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;云谷景区位于黄山东部，上至白鹅岭，下到黄山边境的长形谷地，平均海拔900米左右，总面积1027公顷，可达景点5处，即狮子滚球、千古石、仙人榜、丞相源、灵锡泉，可望景点10处，主要有钵盂峰、眉毛峰、香炉峰、罗汉峰等。景区四周，峰林高耸；松林竹海，郁郁葱葱；深壑幽谷，流水潺潺，云雾缭绕。因宋代丞相程元凤幼时曾在此读书而得名丞相源，明代有位学者游此，见景生情，挥笔手书“云谷”二字，被掷锰禅院住持选中，并定为寺名，从此“云谷”便也成了这一峡谷的地名。</div>
