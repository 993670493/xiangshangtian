        <h3>始信黄山天下奇——北海景区</h3>
       <div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 北海景区（包括西海），位于黄山中部，坐落在光明顶、贡阳山、白鹅岭、始信峰和西海门之间，东连云谷景区，南接玉屏景区，西至吊桥景区，北近松谷景区，海拔1600米左右，是一片高山开阔区域，总面积1318公顷，可达景点25处，主要有始信峰、狮子林、清凉台、曙光亭、黑虎松、连理松、团结松、排云亭、飞来石等，可望景点70余处，主要有梦笔生花、猴子观海、仙人进宝、仙人晒靴，仙人晒鞋、石笋矼、仙桃峰、上升峰等。北海景区以峰为主体，汇集了峰、石、矼、坞、台、松、云等奇景，天工的奇妙布局，自然的色彩变幻，构成了一幅幅伟、奇、幻、险的天然画卷，是黄山的风景窗。</div>
