        <h3>灵幻神奇——松谷景区</h3>
       <div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;松谷景区位于黄山北部，总面积1148公顷。整个景区是个沿着西北向节理发育的谷地，全长10公里，高差近千米。周围群山高耸，形如城廓。区内浓密的原生林带，覆盖率达95%，浓荫蔽日，各类生物相睦共处，繁衍生息。山高蔽日，林幽苔青，千峰竞秀，万木峥嵘。盘龙般的磴道，直伸北海：沿途陡峭的高峰悬崖，蜿蜓起伏的溪流，犹如一幅高悬的立体画卷。景区面积大，线路长，景点景观近百处，可谓奇景荟萃。</div>
